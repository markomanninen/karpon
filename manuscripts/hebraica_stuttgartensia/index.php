$manuscript = array();

$file = __DIR__.'/raw.txt';
$lines = file($file);

foreach ($lines as $r => $line) {
	
	$line = trim($line);
	
	# this symbols may have some meaning on a text, but they doesn't have any numeric correspondence,
	# so they are stripped off to make concordance work better
	$line = str_replace(array(' ×€', '-'), '', $line);
	
	preg_match('|Chapter ([0-9]{1,2})|', $line, $match);
	
	# get chapter number, keep it until next change
	if ($match[1]) {
		$chapter = $match[1];
	}
	
	preg_match('|H([0-9]{1,5})|', $line, $match);
	
	if ($match[1]) {
		
		$words = explode(' ', $line);

		foreach ($words as $i => $word) {
			$word = trim($word);
			if ($word > 0) {
				# get verse number, keep it until next change
				$verse = $word;
			} elseif ($word) {
				# separate string code and word from the string
				preg_match('|H([0-9]{1,5})|', $word, $match);
				
				if (!($strong = $match[0])) {
					/*
					echo "NO STRONG FOR: $chapter:$verse:".(count($manuscript['gen'][$chapter][$verse])+1)."
";
					*/
				}
				$word = str_replace($strong, '', $word);
				# add word item to the manuscript variable
				$manuscript['gen'][$chapter][$verse][] = array($word, $strong, '');
				
			} else {
				#echo 'EMPTY CHAR!';
			}
		}
	}
}

